`timescale 1ns/1ns

module tb_system_top;

    // Глобальные сигналы тактирования
    reg clock_20;
    reg clock_50;

    // Сигналы для связи платы управления (PCON) и Инвертора
    wire fiber_pcon_to_inv; // Оптическая линия от PCON к Инвертору
    wire fiber_inv_to_pcon; // Оптическая линия от Инвертора обратно в PCON

    // Сигналы стороны PCON (модуль ввода-вывода)
    reg [47:0] data_to_send;
    reg        sync_in;
    wire [95:0] data_recieved;
    wire        data_clock;
    wire [2:0]  io_err;

    // Сигналы стороны силового инвертора (INV3PH)
    reg [4:1]  amc_data;
    reg [3:1]  adc_data;
    reg        vdr_err;
    reg        vsens_err;
    reg        npgood;
    reg [6:1]  igbt_err;

    wire [4:1] amc_clk;
    wire [3:1] adc_clk;
    wire [3:1] adc_ncs;
    wire [3:1] ctrl_top;
    wire [3:1] ctrl_bot;
    wire       fo_nen;

    // -------------------------------------------------------------------------
    // 1. Генераторы тактовых частот
    // -------------------------------------------------------------------------
    initial begin
        clock_20 = 0;
        clock_50 = 0;
    end

    always #25 clock_20 = ~clock_20; // 20 МГц (период 50 нс)
    always #10 clock_50 = ~clock_50; // 50 МГц (период 20 нс)


    // -------------------------------------------------------------------------
    // 2. Инстанс модуля ввода-вывода данных (со стороны PCON)
    // -------------------------------------------------------------------------
    INV_DATA_IO u_pcon_io (
        .clockin      (clock_50),           // Тактирование интерфейса 
        .FO_INPUT     (fiber_inv_to_pcon),  // Прием оптики от инвертора 
        .FO_OUTP      (fiber_pcon_to_inv),  // Выход оптики к инвертору [cite: 170]
        .DATA_TO_SEND (data_to_send),       // 48-битный пакет данных 
        .SYNC_IN      (sync_in),            // Импульс синхронизации ШИМ 
        .DATA_RECIEVED(data_recieved),      // Принятая телеметрия [cite: 170]
        .DATA_CLOCK   (data_clock),         // Строб готовности приема [cite: 170]
        .IO_ERR       (io_err)              // Флаги ошибок связи [cite: 170]
    );


    // -------------------------------------------------------------------------
    // 3. Инстанс модуля силового инвертора (INV3PH)
    // -------------------------------------------------------------------------
    INV3PH u_inverter (
        .clock_20  (clock_20),          // Основной такт логики ШИМ [cite: 3]
        .clock_50  (clock_50),          // Такт для вспомогательных узлов [cite: 3]
        .FO_INPUT  (fiber_pcon_to_inv), // Вход оптики от PCON [cite: 4]
        .FO_OUTPUT (fiber_inv_to_pcon), // Выход телеметрии в PCON 
        .FO_nEN    (fo_nen),            // Разрешение оптики 
        .IGBT_ERR  (igbt_err),          // Ошибки драйверов ключей [cite: 4]
        .VDR_ERR   (vdr_err),           // Ошибка питания драйверов [cite: 4]
        .VSENS_ERR (vsens_err),         // Ошибка датчиков [cite: 4]
        .nPGOOD    (npgood),            // Исправность системного питания [cite: 4]
        .CTRL_TOP  (ctrl_top),          // Выходы ШИМ на верхние ключи 
        .CTRL_BOT  (ctrl_bot),          // Выходы ШИМ на нижние ключи 
        .AMC_DATA  (amc_data),          // Сигналы с AMC-изоляторов [cite: 3]
        .AMC_CLK   (amc_clk),           // Тактирование AMC 
        .ADC_DATA  (adc_data),          // Данные с АЦП токов [cite: 3]
        .ADC_CLK   (adc_clk),           // Тактирование АЦП 
        .ADC_NCS   (adc_ncs)            // Выбор кристалла АЦП 
    );


    // -------------------------------------------------------------------------
    // 4. Сценарий тестирования (Алгоритм работы)
    // -------------------------------------------------------------------------
    initial begin
        // Шаг 4.1: Инициализация аппаратных входов силовой платы в "Норму"
        igbt_err  = 6'b111111; // Все 6 драйверов исправны (активная 1) [cite: 4, 30, 31]
        vdr_err   = 0;         // Нет ошибок питания драйверов [cite: 4]
        vsens_err = 0;         // Нет ошибок датчиков [cite: 4]
        npgood    = 0;         // Напряжение питания ПЛИС стабильно (активный 0) [cite: 4]
        amc_data  = 4'b0000;   // Исходные состояния сигма-дельта [cite: 3]
        adc_data  = 3'b111;    // Исходные состояния АЦП [cite: 3]
        
        // Сигналы PCON
        sync_in      = 0;
        data_to_send = 48'h0;

        #2000; // Ждем 2 микросекунды для стабилизации внутренних цепей

        // Шаг 4.2: Формируем пакет уставок и команд для отправки в Инвертор
        // Уставки ШИМ (например: Фаза А = 1000, Фаза Б = 2000, Фаза С = 3000 тактов)
        // Командное слово = 12'b0000_0000_0110 (Включить инвертор=1, Выбрать уставки защиты Case 1)
        data_to_send[11:0]   = 12'd1000; // PWMA [cite: 182]
        data_to_send[23:12]  = 12'd2000; // PWMB [cite: 183]
        data_to_send[35:24]  = 12'd3000; // PWMC [cite: 183]
        data_to_send[47:36]  = 12'b0000_0000_0110; // COMMAND [cite: 184]

        #1000;

        // Автоматически начнется последовательная передача пакетов по кадрам,
        // так как модуль INV_DATA_IO циклически шлет данные при TX_SM_STATE=0[cite: 182].
        // Дадим время на завершение передачи всех 4 кадров данных (около 10-15 мкс)
        #15000;

        // Шаг 4.3: Имитируем приход глобального синхроимпульса ШИМ (PWMSYNC) от PCON
        $display("--- Подача импульса синхронизации ШИМ ---");
        sync_in = 1;
        #5000;  // Удерживаем сигнал (длинный оптический импульс) [cite: 189]
        sync_in = 0;

        // Смотрим на генерацию ШИМ-сигналов на выходах ctrl_top/ctrl_bot
        #100000; 

        // Шаг 4.4: Проверка защитной автоматики (Имитируем аппаратную аварию IGBT)
        $display("--- Имитация аварии драйвера IGBT стойки Фазы А ---");
        igbt_err[1] = 0; // Авария верхнего ключа первой фазы [cite: 30]
        
        #5000; // Смотрим, как мгновенно заблокировались выходы CTRL_TOP/BOT [cite: 66, 67]

        // Конец симуляции
        $display("Симуляция завершена.");
        $finish;
    end

endmodule